// Variablen 
let vollerName = "  Ivonne Franke ";

// Entfernen von überflüssigen Leerzeichen mit trim()
let bereinigterName = vollerName.trim();

// Ausgabe in Kleinbuchstaben mit toLowerCase()
console.log(bereinigterName.toLowerCase());

// Ausgabe in Großbuchstaben mit toUpperCase()
console.log(bereinigterName.toUpperCase());
